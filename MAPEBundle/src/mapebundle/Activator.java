package mapebundle;

import mapebundle.cfgctlr.ConfigController;
import mapebundle.ctxmonitor.ContextMonitor;
import mapebundle.services.IConfigController;
import mapebundle.services.IContextMonitor;

import org.osgi.framework.BundleActivator;
import org.osgi.framework.BundleContext;

public class Activator implements BundleActivator {

	private static BundleContext context;

	public static BundleContext getContext() {
		return context;
	}

	/*
	 * (non-Javadoc)
	 * @see org.osgi.framework.BundleActivator#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext bundleContext) throws Exception {
		Activator.context = bundleContext;
		context.registerService(IContextMonitor.class.getName(), new ContextMonitor(), null);
		context.registerService(IConfigController.class.getName(),new ConfigController(), null);
	}

	/*
	 * (non-Javadoc)
	 * @see org.osgi.framework.BundleActivator#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext bundleContext) throws Exception {
		Activator.context = null;
	}
	


}
